export function normalizeArabic(text: string): string {
  return text.replace(/[هة]/g, "ة").replace(/[ىي]/g, "ي").replace(/[أإآا]/g, "ا").replace(/\s+/g, " ").trim();
}

export function getBetterName(name: string): string | null {
  const normalized = normalizeArabic(name).toLowerCase();
  // Direct match
  for (const [key, value] of Object.entries(FEED_TERMS)) {
    if (normalizeArabic(key).toLowerCase() === normalized) {
      return key !== value ? value : null;
    }
  }
  // Partial match
  for (const [key, value] of Object.entries(FEED_TERMS)) {
    if (normalized.includes(normalizeArabic(key).toLowerCase())) {
      return value;
    }
  }
  return null;
}
